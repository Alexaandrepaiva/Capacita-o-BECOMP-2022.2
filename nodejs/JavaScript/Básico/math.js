// Math é um objeto disponibilizado pela própria linguagem

let num1 = 9.54578;
let num2 = 10.501;
let num3 = 53;

console.log(Math.floor(num1)); // arredonda para cima
console.log(Math.ceil(num1)); // arredonda para baixo
console.log(Math.round(num1)); // arredonda

console.log(Math.min(num1, num2, num3)) // mostra o mínimo valor da lista
console.log(Math.max(num1, num2, num3)) // mostra o máximo valor da lista

console.log(Math.random()); // MOstar um número aleatório entre 0 e 1, exlcuídos